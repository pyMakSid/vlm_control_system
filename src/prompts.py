ALLOWED_COMMANDS = [
    "TAKEOFF",      
    "LAND",
    "HOVER",        
    "FORWARD",
    "BACKWARD",
    "LEFT",
    "RIGHT",
    "UP",
    "DOWN",
    "YAW_LEFT",
    "YAW_RIGHT",
    "STOP",
]


def command_json_schema() -> dict:
    """JSON-схема ответа для constrained-декодирования на сервере.

    Заставляет модель вернуть валидный объект со строго одной из допустимых
    команд (enum) и числовым confidence — убирает мусорный формат и пропуски
    полей у маловесных моделей.
    """
    return {
        "type": "object",
        "properties": {
            "command": {"type": "string", "enum": ALLOWED_COMMANDS},
            "reason": {"type": "string"},
            "confidence": {"type": "number", "minimum": 0.0, "maximum": 1.0},
        },
        "required": ["command", "reason", "confidence"],
        "additionalProperties": False,
    }

def build_system_prompt(context_frames: int = 1, replay_decisions: bool = False) -> str:
    """Собирает системный промпт под фактический режим контекста.

    context_frames — сколько кадров реально передаётся (1 = только текущий);
    replay_decisions — добавляются ли к прошлым кадрам ранее выданные команды.
    Промпт не должен упоминать данные, которых модель не получает, иначе она
    галлюцинирует «предыдущий кадр»/«прошлую команду».
    """
    intro = "Ты — модуль управления беспилотным летательным аппаратом (БПЛА).\n"

    if context_frames > 1:
        intro += (
            "На вход ты получаешь несколько последних кадров с бортовой камеры "
            "(от старого к свежему; последний — текущий) и текстовое описание "
            "миссии.\n"
        )
        if replay_decisions:
            intro += (
                "К прошлым кадрам приложены команды, которые ты уже выдал по ним. "
            )
        intro += (
            "Оценивай по последовательности движение сцены, но решение принимай "
            "по ТЕКУЩЕМУ (последнему) кадру.\n"
        )
    else:
        intro += (
            "На вход ты получаешь ОДИН текущий кадр с бортовой камеры и текстовое "
            "описание миссии. Прошлых кадров нет — анализируй только этот кадр.\n"
        )

    body = f"""Твоя задача — выдать РОВНО ОДНУ команду управления для текущего кадра.

Кадр может быть предварительно размечен детектором YOLO: на нём нарисованы
зелёные рамки с подписями объектов, а к промпту добавлена их текстовая сводка
с положением. Используй эту разметку как подсказку, но опирайся и на саму сцену.

Допустимые команды (выбери строго одно из этих значений):
{", ".join(ALLOWED_COMMANDS)}

Правила ответа:
1. Отвечай ТОЛЬКО валидным JSON-объектом, без markdown и текста вне JSON.
2. Формат: {{"command": "<одна из допустимых команд>", "reason": "<краткое обоснование на русском>", "confidence": <число от 0.0 до 1.0>}}
3. Поле command — это РЕШЕНИЕ (что делать прямо сейчас), оно должно
   соответствовать reason. Не копируй в reason список команд или текст задания.
4. Если ситуация неясна или кадр непригоден — возвращай "HOVER" с низкой уверенностью.
5. Безопасность важнее миссии: при риске столкновения выбирай "STOP".
"""
    return intro + body


